import array
import numpy as np
# Exemplo de Lista em Python
lista = [1, 2, 3, 4, 5]
print("Lista:", lista)

# Exemplo de Array usando o módulo array
arr = array.array('i', [1, 2, 3, 4, 5])  # 'i' indica inteiros
print("Array:", arr)

# Exemplo de Vetor usando o módulo numpy
vetor = np.array([1, 2, 3, 4, 5])
print("Vetor (NumPy):", vetor)